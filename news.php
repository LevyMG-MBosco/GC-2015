            <h1 class="modal-title">General Carbide Corporation Holding Company Picnic on Friday, July 26, to Celebrate 45 Years in Business</h1>
          </div>
          <div class="modal-body">
            <h2>Tyler Courtney, Westmoreland County Commissioner, to deliver proclamation</h2>
            <p>July 25, 2013</p>
            <p>Greensburg, PA, July 25, 2013 — General Carbide Corporation, a manufacturer of more than 50
               grades of tungsten carbide tooling for a variety of cutting and metal forming applications, will be
               observing its 45th anniversary with a picnic at the company's Hempfield Township facility (1151
               Garden Street; Greensburg, PA 15601) on Friday, July 26, from 12:00 noon-3:00 p.m. Tyler
               Courtney, a Westmoreland County Commissioner, will be delivering a proclamation to
               acknowledge the company's longevity, and its position as a market leader for nearly half a
               century. Commissioner Courtney will present the proclamation to Lorraine Pappafava, whose
               late husband, Premo, started the company in 1968.</p>
            <p>“This occasion is all about family,” says Mona Pappafava-Ray, General Carbide's president and
               CEO. “A number of our employees have been with us for 20, 30 or 40 years. There's a long
               history with them, and it makes me proud that they've been so loyal.”</p>
            <p>Ms. Pappafava-Ray has known many of her employees for decades because she has been with
               the company full-time since 1986, upon graduating from Carnegie Mellon University with a
               degree in Industrial Management. Before Ms Pappafava-Ray became president 20 years ago
               and added CEO to her responsibilities 11 years ago, when her father passed away, she held a
               variety of staff and management positions in the organization.</p>
            <p>To acknowledge the contributions that General Carbide's 200 employees have made to the
               company's success, Ms. Pappafava-Ray is giving each of them a logo jacket, a card inscribed
               with a handwritten personalized message.</p>
            <p>When it comes to giving workers the necessary tools to succeed, Ms. Pappafava-Ray spares no
               expense. “Every year, we invest at least $1 million in our facilities because it's the only way to
               stay competitive,” she says. This year, however, General Carbide has purchased $2 million
               worth of lathes, presses and other machinery. “It's more than we usually dedicate to capital
               equipment, but it's money well-spent to ensure that we continue to operate efficiently.”</p>
            <p>Since the company's humble beginnings, General Carbide has given back to the community by
               supporting a variety of civic, cultural, educational and religious organizations. Today, the
               company directs the majority of its philanthropy to Westmoreland County Community College,
               the Westmoreland Cultural Trust and Westmoreland Excela Health.</p>
            <p>Mike Langer, president of the Westmoreland Cultural Trust, is appreciative of the company's
               interest in the organization he heads. “Mona is the epitome of balance between work ethic,
               devotion to family, and service to the community. The Trust and Westmoreland County have
               benefitted greatly from her presence and commitment.” The Trust manages community assets
               and promotes the arts and culture throughout Westmoreland County.</p>
            <p>“Continuing in the footsteps of her late father, Premo Pappafava, a longtime member of the Westmoreland County Community College (WCCC) Educational Foundation, Mona has adopted
               the belief that education is a positive way to affect someone's life,'” adds Daniel Obara, WCCC's
               president. “We are grateful for General Carbide's ongoing support of the WCCC Foundation
               Scholarship Golf Classic which raises funds to provide scholarships for many students in pursuit
               of their educational goals.”</p>
            <p>At the regional level, General Carbide, in conjunction with the Pittsburgh Business Times,
               established the Premo J. Pappafava Excellence Award last year to recognize the
               accomplishments of multi-generational, family-owned manufacturers throughout western
               Pennsylvania, West Virginia and eastern Ohio for their overall excellence, innovation, ethics and
               philanthropy. Beginning this year, the Award will be given annually to a company in the Tri-State
               Area.</p>
            <p>Ms. Pappafava-Ray and her management team also encourage employees to become involved
               in their communities. To promote employees taking on those responsibilities, the company
               provides financial support to non-profit organizations in which employees are involved, such as
               youth sports leagues, scouting and civic groups.</p>
            <p>“People are at their best when they have interests outside of the workplace to balance their lives,”
               says Ms. Pappafava-Ray. “We're proud that we're able to help our employees engage in
               activities they enjoy, and which enhance the quality of life in their communities.”</p>
            <p>Focusing inward, Ms. Pappafava-Ray honors employees at the company's annual family picnic
               for their service to the company. “It's important that our employees their families know how much
               we appreciate their contributions to our success,” she says. This year's family picnic will be held
               on Sunday, August 11, at Idlewild Park.</p>
            <p><span class="red-text">Taking Care of “Family”</span></p>
            <p>General Carbide has always prided itself on having a “family atmosphere” where people enjoy
               working and take tremendous pride in their work. Ms. Pappafava-Ray demonstrated that attitude
               by weathering the recession without choosing to lay off any employees. Instead, she reduced
               salaries and wages from 10 to 30 percent, including her own, between October 2008 and
               September 2009. This bold decision was a risk, not knowing how long the recession would last.
               She knew, however, that a long-term commitment to her employees would pay off when
               business conditions improved, largely due to the management team and production employees
               working aggressively to develop new business opportunities when production levels were low.</p>
            <p>The financial implications were great, and a slow recovery would have been detrimental to the
               bottom line, and to the company's survival. Instead, General Carbide has been well-prepared to
               take advantage of new opportunities due to the workforce remaining intact, and then expanding,
               while many competitors cut their employment levels, or went out of business altogether.</p>
            <p>In appreciation of employees' loyalty during difficult times, Ms. Pappafava-Ray gave back part of
               the salaries and wages in December 2010 which were sacrificed in 2008-09, providing an
               unexpected “Christmas bonus” to the company's salaried employees. As orders continued to
               climb during the first half of calendar year 2011, Ms. Pappafava-Ray gave back the remaining
               amounts of salaries and wage cuts in mid-June of 2011, pleasantly surprising employees once
               again by completely repaying them for their lost wages.</p>
