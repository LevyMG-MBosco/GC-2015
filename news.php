            <h1 class="modal-title">General Carbide Corporation Receives ISO 9001:2008 Certification</h1>
          </div>
          <div class="modal-body">
            <p>July 30, 2014</p>
            <p>General Carbide Corporation, a manufacturer of more than 50 grades of tungsten carbide tooling
               for a variety of industrial wear, metal forming and stamping applications, has earned ISO
               certification. Until now, the company had been ISO-compliant. General Carbide’s desire to obtain
               ISO certification results from the company’s ongoing commitment to improve its quality systems
               and processes. By doing so, General Carbide can serve customers more effectively than ever
              and continue to grow by taking advantage of new business opportunities in a range of markets,
               including automotive, oil & gas, and aerospace.</p>
            <p>“I am pleased that we have made this significant investment in our future,” says Mona PappafavaRay,
                General Carbide’s president and CEO. “Being ISO-certified ensures our customers that we
                 have an effective quality system in-place. It also enables us to develop business opportunities in
                 markets that were difficult to penetrate without certification. Becoming ISO-certified is a natural
                 extension of our ongoing investments in production equipment, enterprise resource planning
                 systems and information technology.”</p>
            <p>Ms. Pappafava-Ray also believes that by standardizing continual improvement processes,
               corrective and preventive procedures, and customer feedback mechanisms, General Carbide’s
               customers will experience superior product consistency and order turnaround time.</p>
            <p>The company’s ISO certification occurs at a time when sales have been strong for almost five
               years. “The fact that we have out-performed our competition is a testament to the hard work and
               dedication of our management team and production staff,” says Ms. Pappafava-Ray. “Obtaining
              ISO certification strengthens our competitive advantage by complementing the innovative
               thinking, technical expertise and personalized service that have allowed us to thrive in
               challenging times.”</p>
