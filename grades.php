              <h1>About Carbide</h1>
              <p>Cemented carbide is produced by mixing a metal carbide, such as tungsten, with a metallic binder material that is usually cobalt, nickel or a combination of both. This mixture is generally held together by an organic binder and formed into a desired shape, known as a preform.</p>
              <p>The preform is then placed into a furnace for sintering, which melts the metallic binder material around the carbide particles. During sintering, the preform shrinks volumetrically about 40%. The last step is grinding the preform to its final dimensions.</p>
              <span class="red-text">Carbide Properties</span>
              <p>Cemented carbide products have excellent mechanical and physical characteristics.</p>
              <span class="red-text">Abrasion Resistance</span>
              <p>Carbide's exceptional resistance to abrasion is its most important property. In abrasive applications, carbides can outlast some wear-resistant steel alloys by a factor of 100 to 1.</p>
              <span class="red-text">Deflection Resistance</span>
              <p>Cemented carbides have a high modulus of elasticity that provides minimum deflection when exposed to bending forces. In fact, the modulus of elasticity is three times that of steel.</p>
              <span class="red-text">Corrosion Resistance</span>
              <p>Since carbides are generally chemically inert (considering binding material as a factor), they can be used successfully in many chemical and corrosive environments.</p>
              <span class="red-text">Torsional Strength</span>
              <p>With a torsion modulus twice that of high-speed steel, carbide is the preferred material for rotating applications.</p>
              <span class="red-text">Compressive Strength</span>
              <p>Some grades of carbide with cobalt binder perform flawlessly under ultra-high compression and have been used very successfully in pressure applications at up to one million psi.</p>
              <span class="red-text">Toughness</span>
              <p>Carbide grades with higher binder contents have excellent resistance to impact.</p>
              <span class="red-text">Low Temperature Wear Resistance</span>
              <p>Even at cryogenic temperatures as low as -453°F, carbides retain good wear resistance and offer a relatively low coefficient of friction where lubricants cannot be used.</p>
              <span class="red-text">High Temperature Wear Resistance</span>
              <p>At 1,000°F, carbides still maintain over 90% hardness. Certain grades can even retain significant strength at 2,000°F (higher at intermittent temperatures).</p>
